/* Classe Math */

// Potenciação 
console.log(Math.pow(2, 3));

// Arredonda para o inteiro mais próximo
console.log(Math.round(.6));

// Arredonda para cima ou para baixo
console.log(Math.ceil(.6));
console.log(Math.floor(.6));

// Valor absoluto
console.log(Math.abs(-20));

// Retorna o maior argumento
console.log(Math.max(2, 5, 6));

// Retorna o menor argumento
console.log(Math.min(4, 2, 9));

// Numero randomico
console.log(Math.random());

// PI e E
console.log(Math.PI);
console.log(Math.E);

// Raiz quadrada 
console.log(Math.sqrt(4));