# REPETIÇÃO FOR
for numero in (1..100)
    puts "Numero: #{numero}"
end

# REPETIÇÃO WHILE
numero = 0
while numero <= 100
    puts "Numero: #{numero}"
    numero += 1
end

# REPETIÇÃO UNTIL
numero = 0
until numero == 100
    puts "Numero: #{numero}"
    numero += 1
end

